fl. oz.) with shower cap included.</li>
</ol>
<h2>Warnings and Precautions</h2>
Topical corticosteroids when used over large areas for prolonged periods and under an occlusive dressing can be absorbed into the bloodstream and cause reversible adrenal suppression. Children and infants are particularly susceptible to adrenal suppression related to use of topical corticosteroids.

Before using Derma-Smoothe/FS<sup>®</sup> Topical Oil, talk to your doctor or pharmacist if:
<ol>
 	<li>your child is between 3 months and one year of age.</li>
 	<li>you or your child has a peanut allergy or sensitivity.</li>
 	<li>you have other inflammatory skin diseases as a result of impaired circulation (stasis dermatitis).</li>
 	<li>you are pregnant or planning to become pregnant.</li>
 	<li>you are breast feeding.</li>
</ol>
Avoid getting Derma-Smoothe/FS<sup>®</sup> Topical Oil in the eye, or other mucous membrane.

Do not use occlusive dressings such as a bandage, or cover the treated areas tightly.

Derma-Smoothe/FS<sup>®</sup> Topical Oil should not be used to treat diaper dermatitis. Derma-Smoothe/FS<sup>®</sup> Topical Oil should not be applied to the diaper area as diapers or plastic pants may constitute occlusive dressing.

Derma-Smoothe/FS<sup>®</sup> Topical Oil should be used with caution on the underarm or groin. This medication is not recommended for use on the face.

If you are over 65 years of age, use Derma-Smoothe/FS<sup>®</sup> Topical Oil with caution.
<h2>Interactions With This Medication</h2>
Tell your doctor or pharmacist about all your other medications, including medicines that you bought without a prescription and natural health products. Especially if you are taking the following medications:
<ol>
 	<li>insulin, or medications for high blood sugar</li>
 	<li>diuretics (water pills)</li>
 	<li>amphotericin-B (an anti-fungal medication)</li>
 	<li>non-steroidal anti-inflammatory medications, salicylates (e.g. aspirin)</li>
 	<li>blood thinning medications</li>
 	<li>estrogen</li>
 	<li>phenobarbital or phenytoin (anti-seizure medications)</li>
 	<li>rifampin (antibiotic).</li>
</ol>
Since it is not known if Derma-Smoothe/FS<sup>®</sup> Topical Oil can be used with other treatments, it is recommended that Derma-Smoothe/FS<sup>®</sup> not be used with other treatments.
<h2>Proper use of This Medication</h2>
Not for oral, ophthalmic, or intravaginal use.
<h3>Usual dose:</h3>
Apply the least amount of Derma-Smoothe/FS<sup>®</sup> Topical Oil needed to cover the affected area. Avoid contact with the eyes. In case of contact, wash eyes liberally with water. Do not use Derma-Smoothe/FS<sup>®</sup> Topical Oil with occlusive dressings, diapers or plastic pants.
<h3>For Adult Atopic Eczema.</h3>
Moisten the affected area with water. Apply a thin film of Derma-Smoothe/FS<sup>®</sup> Topical Oil to the affected area and massage in gently. Use two to three times daily. For use on the scalp: Wet hair and scalp, then apply thin film of oil to entire scalp. Massage scalp and cover with supplied shower cap overnight (4 hours minimum).
<h3>For Atopic Dermatitis in Children 3 months to 12 years.</h3>
Moisten the affected area with water. Apply a thin film of Derma-Smoothe/FS<sup>®</sup> Topical Oil to the affected area and massage in gently. Use twice daily for a maximum of 4 weeks. Do not use on the face or in the diaper or anogenital area. If no improvement is seen within 2 weeks, contact your child’s doctor.
<h3>Overdose:</h3>
If you have applied more Derma-Smoothe/FS<sup>®</sup> Topical Oil than you should, wash thoroughly with water and contact your doctor or your nearest poison control centre, or go to the emergency room of your local hospital.
<h3>Missed Dose:</h3>
If a dose of this medication is missed, it is not necessary to make up the missed dose. Skip the missed dose and continue with the next scheduled dose.
<h2>Side Effects and What to Do About Them</h2>
Common side effects with Derma-Smoothe/FS<sup>®</sup> Topical Oil include:
<ol>
 	<li>redness, burning, itching, irritation, rash</li>
 	<li>loss of skin colour (hypopigmentation)</li>
 	<li>darkening of skin colour (hyperpigmentation)</li>
 	<li>abscess (skin infection)</li>
</ol>
There have been reports of reactions to Derma-Smoothe/FS<sup>®</sup> Topical Oil in children thought to be related to peanut allergy.

Other side effects reported with the use of topical corticosteroids in general include:
<ol>
 	<li>dryness, inflammation of hair follicles, abnormal hair growth</li>
 	<li>stretch marks</li>
 	<li>secondary infection, acne</li>
 	<li>allergic skin reaction</li>
 	<li>miliaria (heat rash)</li>
 	<li>skin thinning</li>
 	<li>rash around the mouth</li>
 	<li>increased blood sugar</li>
</ol>
Serious side effects such as Cushing's syndrome (a hormonal disorder) may be associated with systemic absorption from improper or excessive use of topical corticosteroids. Adrenal suppression, linear growth retardation, delayed weight gain and intracranial hypertension (increased pressure around the brain) have also been reported with the use of topical corticosteroids in infants and children.
