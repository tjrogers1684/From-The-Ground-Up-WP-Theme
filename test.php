<h2>How to Store it</h2>
Keep tightly closed. Store at 25°C (77°F); excursions permitted to 15°-30°C (59°-86°F) [see USP Controlled Room Temperature].

Keep out of the reach of children and pets.

Medicines should not be disposed of down the drain or in household garbage. Ask your pharmacist how to dispose of medicines no longer required. These measures will help to protect the environment.
<h2>Reporting Side Effects</h2>
You can report any suspected side effects associated with the use of health products to Health Canada by:
<ul>
 	<li>Visiting <a href="https://www.canada.ca/en/health-canada/services/drugs-health-products/medeffect-canada/adverse-reaction-reporting.html">Adverse Reaction Reporting</a> for information on how to report online, by mail or by fax; or</li>
 	<li>Calling toll-free at 1-866-234-2345.</li>
</ul>
NOTE: Contact your health professional if you need information about how to manage your side effects. The Canada Vigilance Program does not provide medical advice.
