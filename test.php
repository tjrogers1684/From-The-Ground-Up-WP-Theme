Évitez tout contact de l’huile topique Derma- Smoothe/FS<sup>®</sup> avec les yeux ou les muqueuses.

N’utilisez pas de pansement occlusif, comme un bandage, et ne recouvrez pas les régions traitées d’un pansement ou d’un vêtement trop serré.

L’huile topique Derma-Smoothe/FS<sup>®</sup> ne doit pas servir au traitement de l’irritation de la peau due aux couches. Elle ne doit pas être appliquée sous la couche, car cette dernière ou la culotte en plastique peuvent être occlusives.

L’huile topique Derma-Smoothe/FS<sup>®</sup> doit être utilisée avec prudence sur les lésions des aisselles ou de l’aine. L’emploi de ce produit sur le visage n’est pas recommandé.

Si vous avez plus de 65 ans, vous devez utiliser l’huile topique Derma-Smoothe/FS<sup>®</sup> avec prudence.
<h2>Interactions Médicamenteuses</h2>
Il est important d'informer votre médecin ou votre pharmacien de tous les médicaments que vous prenez, y compris les médicaments vendus sans ordonnance et tout produit à base de plantes médicinales. Il est particulièrement important de le dire à votre médecin ou à votre pharmacien si vous prenez un ou plusieurs des médicaments suivants:
<ol>
 	<li>insuline ou autres médicaments pour abaisser le taux de sucre dans le sang;</li>
 	<li>diurétiques (médicaments qui font uriner);</li>
 	<li>amphotéricine B (antifongique);</li>
 	<li>anti-inflammatoires non stéroïdiens, salicylés (p. ex., l’aspirine);</li>
 	<li>médicaments qui éclaircissent le sang;</li>
 	<li>œstrogènes;</li>
 	<li>phénobarbital ou phénytoïne (médicaments contre les convulsions);</li>
 	<li>rifampicine (antibiotique).</li>
</ol>
Comme on ignore si l’huile topique Derma- Smoothe/FS<sup>®</sup> peut être utilisée avec d’autres médicaments, on recommande de l’utiliser seule.
<h2> Utilisation Appropriée De Ce Médicament</h2>
Non destiné à un usage oral, opthalmique ou intravaginal.
<h3>Dose habituelle:</h3>
Appliquez la plus<sup>®</sup> petite quantité d’huile topique Derma-Smoothe/FS nécessaire pour couvrir les régions touchées. Évitez tout contact avec les yeux. En cas de contact accidentel avec les yeux, rincez abondamment avec de l’eau. L’huile topique Derma-Smoothe/FS<sup>®</sup> ne doit pas être appliquée sous pansement occlusif ni sous une couche ou une culotte en plastique.
<h3>Traitement de l’eczéma atopique chez les adultes:</h3>
Humectez la région touchée avec de l’eau, puis appliquez une mince couche d’huile topique Derma- Smoothe/FS<sup>®</sup> 2 ou 3 fois par jour, en massant légèrement de façon à faire pénétrer le produit. Pour usage sur le cuir chevelu: Mouiller les cheveux et le cuir chevelu, puis appliquer une mince couche d’huile sur l’ensemble du cuir chevelu. Masser le cuir chevelu et porter le bonnet fourni toute la nuit (au moins 4 heures).
<h3>Traitement de la dermatite atopique chez les enfants âgés de 3 mois à 12 ans:</h3>
Humectez la région touchée avec de l’eau, puis appliquez une mince couche d’huile topique Derma-Smoothe/FS<sup>®</sup> 2 fois par jour, en massant légèrement de façon à faire pénétrer le produit. La durée du traitement ne doit pas excéder 4 semaines. N’appliquez pas le produit sur le visage, sous la couche, ni sur la région anogénitale. En l’absence d’amélioration après 2 semaines de traitement, communiquez avec le médecin de votre enfant.
<h3>Surdosage:</h3>
Si vous avez appliqué une quantité excessive d’huile topique Derma-Smoothe/FS<sup>®</sup>, rincez abondamment les régions touchées avec de l’eau, puis communiquez avec votre médecin ou le centre antipoison de votre région, ou rendez-vous au Service des urgences d’un centre hospitalier.
<h3>Dose oubliée:</h3>
Si vous oubliez une application de ce médicament, vous ne devez pas compenser la dose oubliée. Sautez la dose oubliée et poursuivez le traitement en faisant l’application suivante à l’heure habituelle.
<h2>Effets Secondaires Et Mesures À Prendre</h2>
Voici les effets secondaires souvent associés à l’emploi de l’huile topique Derma-Smoothe/FS<sup>®</sup>:
<ol>
 	<li>rougeur, sensation de brûlure, démangeaisons, irritation et éruptions cutanées;</li>
 	<li>décoloration de la peau (hypopigmentation);</li>
 	<li>coloration plus foncée de la peau (hyperpigmentation);</li>
 	<li>abcès (infection de la peau).</li>
</ol>
Certaines réactions à l’huile topique Derma- Smoothe/FS<sup>®</sup>, que l’on croit attribuables à une allergie aux arachides, ont été signalées chez des enfants.

Voici d'autres effets secondaires associés aux corticostéroïdes topiques en général:
<ol>
 	<li>sécheresse, inflammation des follicules pileux, développement exagéré du système pileux;</li>
 	<li>vergetures;</li>
 	<li>surinfection, acné;</li>
 	<li>réaction allergique cutanée;</li>
 	<li>miliaire (boutons de chaleur);</li>
 	<li>amincissement de la peau;</li>
 	<li>éruption cutanée autour de la bouche;</li>
 	<li>augmentation de la glycémie (taux de sucre dans le sang).</li>
</ol>
Des effets indésirables graves, tels que le syndrome de Cushing (un trouble hormonal), sont parfois associés à l’absorption générale de corticostéroïdes topiques par suite d’un usage inapproprié ou excessif de ces médicaments. Des cas de suppression surrénalienne, de retard de croissance de la taille, de ralentissement du gain de poids, ainsi que d’hypertension intracrânienne (augmentation de la pression autour du cerveau) ont également été signalés chez des nourrissons et des enfants à la suite de l’utilisation de corticostéroïdes topiques.
