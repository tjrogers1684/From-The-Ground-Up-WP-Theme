<h2>Comment Conserver Le Médicament</h2>
Gardez le flacon hermétiquement fermé. Conservez le produit à une température de 25ºC (77ºF), avec écarts permis entre 15 et 30ºC (entre 59 et 86ºF) [voir Température ambiante contrôlée USP].
Gardez ce médicament hors de la portée des enfants et des animaux de compagnie.

Les médicaments ne doivent pas être jetés dans les toilettes ou l'évier, ni avec les ordures ménagères. Demandez à votre pharmacien comment vous débarrasser des médicaments dont vous n'avez plus besoin. Ces mesures contribuent à protéger l'environnement.
